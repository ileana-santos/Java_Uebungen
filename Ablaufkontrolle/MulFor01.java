public class MulFor01 {
    public static void main(String[] args) {

        //Multiple For Loops
        //Columns and Rows
        for (int i = 0; i < 5; i++) {

            for( int j = 0; j <  4; j++) {
                System.out.print("i= " + i+ " j= " + j + "\t");
            }
            System.out.println();
        }
    }
}
