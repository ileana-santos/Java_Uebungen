public class For01 {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.println("i = " + i);

            /*  1. Initialisierung Schleifenvariable i = 0;
                2. if i < 10, Kondition erfüllt dann in die Scheibenkörper,
                   wenn nicht aus der Schleife
                3. Code in Scheibenkörper wird durchgeführt
                4. Iteration i++
                5. Programmkontrolle geht wieder zu Anfang der Schleife 2.
             */
        }
        System.out.println("Nach der Schleife geht's weiter!");
    }
}